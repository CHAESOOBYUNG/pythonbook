i = int(input("입력: "))

if i % 2 == 0:
    print("2로 나누어 떨어지는 숫자입니다.")
else:
    print("2로 나누어 떨어지는 숫자가 아닙니다.")

if i % 3 == 0:
    print("3으로 나누어 떨어지는 숫자입니다.")
else:
    print("3으로 나누어 떨어지는 숫자가 아닙니다.")

if i % 4 == 0:
    print("4로 나누어 떨어지는 숫자입니다.")
else:
    print("4로 나누어 떨어지는 숫자가 아닙니다.")

if i % 5 == 0:
    print("5로 나누어 떨어지는 숫자입니다.")
else:
    print("5로 나누어 떨어지는 숫자가 아닙니다.")