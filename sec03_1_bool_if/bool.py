# 논리 연산자
## 단항 not
not True # False
not False # True

## 이항 and 
# 둘다 True 일 때 결과 True
True and True # True
True and False # False
False and True # False
False and False # False

## 이항 or
# 둘 중 하나만 True이면 결과 True
True or True # True
True or False # True
False or True # True
False or False # False



